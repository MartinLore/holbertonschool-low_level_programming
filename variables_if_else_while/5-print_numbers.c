/**
 * main - The above function prints the numbers 0 to 9 on separate lines.
 * 
 * Return: The main function is returning an integer value of 0.
 */
#include <stdio.h>

int main(void)
{
    int i;

    for (i = 0; i < 10; i++)
    {
        printf("%d", i);
    }
    printf("\n");

    return (0);
}
